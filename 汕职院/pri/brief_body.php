    <h2>&nbsp;&nbsp;历史沿革</h2>
    <p class="brief-mes">
      汕头职业技术学院成立于2002年3月29日。它的前身为广东汕头教育学院（1982-2002年）、汕头幼师（1970-2002年）、汕头商业供销学校（1964-2002年）、汕头机电学校（1976-2002年）。2002年3月经广东省人民政府批准成立。
    </p>

    <p class="brief-mes">
      学院总建筑面积23.5万平方米，教学、实验实习设备总值约4368万元，图书藏量85.58万册。学院拥有完善的教学、生活、运动等设施；有总值1300万元的能满足各专业教学、实习需要的实验实习设备；图书馆藏书总数达30万册，是青年求学、深造的理想校园。
    </p>
